def weighted_brier(y_true, y_prob, weights=None):
    if weights is None:
        return brier_score_loss(y_true, y_prob)
    w = weights / weights.sum()
    return float(np.sum(w * (y_true - y_prob) ** 2))

def weighted_auroc(y_true, y_prob, weights=None):
    return roc_auc_score(y_true, y_prob,
                         sample_weight=weights)

def weighted_auprc(y_true, y_prob, weights=None):
    return average_precision_score(y_true, y_prob,
                                   sample_weight=weights)

def compute_ece(y_true, y_prob, weights=None, n_bins=15):
    """Expected Calibration Error — 15 equal-width bins."""
    bin_edges = np.linspace(0, 1, n_bins + 1)
    ece = 0.0
    total_w = len(y_true) if weights is None \
              else weights.sum()

    for i in range(n_bins):
        mask = (y_prob >= bin_edges[i]) & \
               (y_prob < bin_edges[i+1])
        if mask.sum() == 0:
            continue
        if weights is None:
            bin_w    = mask.sum()
            bin_acc  = y_true[mask].mean()
            bin_conf = y_prob[mask].mean()
        else:
            bin_w    = weights[mask].sum()
            bin_acc  = np.average(
                y_true[mask], weights=weights[mask])
            bin_conf = np.average(
                y_prob[mask], weights=weights[mask])
        ece += (bin_w / total_w) * abs(bin_acc - bin_conf)
    return ece

def threshold_metrics(y_true, y_prob, threshold,
                      weights=None):
    """Compute threshold-dependent metrics."""
    y_pred = (y_prob >= threshold).astype(int)
    tn, fp, fn, tp = confusion_matrix(
        y_true, y_pred).ravel()

    # Weighted versions
    if weights is not None:
        w = weights
        tp_w = w[(y_true==1) & (y_pred==1)].sum()
        fn_w = w[(y_true==1) & (y_pred==0)].sum()
        fp_w = w[(y_true==0) & (y_pred==1)].sum()
        tn_w = w[(y_true==0) & (y_pred==0)].sum()
        tpr = tp_w / (tp_w + fn_w) if (tp_w+fn_w)>0 else 0
        fpr = fp_w / (fp_w + tn_w) if (fp_w+tn_w)>0 else 0
        ppv = tp_w / (tp_w + fp_w) if (tp_w+fp_w)>0 else 0
        acc = (tp_w + tn_w) / w.sum()
    else:
        tpr = tp / (tp + fn) if (tp+fn) > 0 else 0
        fpr = fp / (fp + tn) if (fp+tn) > 0 else 0
        ppv = tp / (tp + fp) if (tp+fp) > 0 else 0
        acc = (tp + tn) / len(y_true)

    f1  = 2*tpr*ppv / (tpr+ppv) if (tpr+ppv) > 0 else 0
    return {
        'TPR': tpr, 'FPR': fpr,
        'PPV': ppv, 'F1' : f1,
        'ACC': acc,
    }

def fairness_metrics(y_true, y_prob, threshold,
                     income_groups, weights=None):
    """
    Compute fairness metrics between Low and Higher
    income groups.
    """
    results = {}
    for grp in ['Low', 'Higher', 'Unknown']:
        mask = income_groups == grp
        if mask.sum() == 0:
            continue
        w_grp = weights[mask] if weights is not None \
                else None
        m = threshold_metrics(
            y_true[mask], y_prob[mask],
            threshold, weights=w_grp)
        auc_grp = weighted_auroc(
            y_true[mask], y_prob[mask],
            weights=w_grp)
        m['AUROC'] = auc_grp
        results[grp] = m

    # Primary fairness gaps: Low vs Higher
    gaps = {}
    if 'Low' in results and 'Higher' in results:
        for metric in ['TPR', 'FPR', 'PPV', 'AUROC']:
            gaps[f'{metric}_Gap'] = (
                results['Low'][metric] -
                results['Higher'][metric])
        # DPD: P(y_pred=1|Low) - P(y_pred=1|Higher)
        # = TPR*prev + FPR*(1-prev) per group
        # Simplified: use raw prediction rates
        for grp in ['Low', 'Higher']:
            mask = income_groups == grp
            if weights is not None:
                w_grp = weights[mask]
                pred_rate = np.average(
                    (y_prob[mask] >= threshold).astype(float),
                    weights=w_grp)
            else:
                pred_rate = (
                    y_prob[mask] >= threshold).mean()
            gaps[f'pred_rate_{grp}'] = pred_rate
        gaps['DPD'] = (gaps['pred_rate_Low'] -
                       gaps['pred_rate_Higher'])

    return results, gaps

