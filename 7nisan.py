def compute_metrics_bootstrap(
        y_true, y_prob_dict, threshold_dict,
        income_groups, weights=None):
    """
    Compute all metrics on a single bootstrap resample.
    y_prob_dict : {(model, regime): y_prob array}
    threshold_dict: {(model, regime): threshold}
    Returns dict of metrics.
    """
    n     = len(y_true)
    idx   = np.random.choice(n, n, replace=True)
    y_b   = y_true[idx]
    inc_b = income_groups[idx]
    w_b   = weights[idx] if weights is not None else None

    metrics = {}
    for (model_name, regime), y_prob in y_prob_dict.items():
        y_p   = y_prob[idx]
        thr   = threshold_dict[(model_name, regime)]['youden']
        thr_fx = 0.5

        # Regime-consistent weights
        eval_w = w_b if regime in ['B','C'] else None

        # Threshold-free
        try:
            auc = roc_auc_score(y_b, y_p,
                                sample_weight=eval_w)
        except Exception:
            auc = np.nan
        try:
            brier = weighted_brier(y_b, y_p, eval_w)
        except Exception:
            brier = np.nan
        try:
            auprc = average_precision_score(
                y_b, y_p, sample_weight=eval_w)
        except Exception:
            auprc = np.nan

        # Threshold-dependent (Youden-J)
        y_pred_yj = (y_p >= thr).astype(int)
        tm_yj     = _threshold_metrics_fast(
            y_b, y_pred_yj, eval_w)

        # Threshold-dependent (Fixed-0.5)
        y_pred_fx = (y_p >= thr_fx).astype(int)
        tm_fx     = _threshold_metrics_fast(
            y_b, y_pred_fx, None)

        # Fairness (Youden-J)
        fair_yj = _fairness_gaps_fast(
            y_b, y_p, thr, inc_b, eval_w)

        # Fairness (Fixed-0.5)
        fair_fx = _fairness_gaps_fast(
            y_b, y_p, thr_fx, inc_b, None)

        metrics[(model_name, regime)] = {
            'AUROC'   : auc,
            'Brier'   : brier,
            'AUPRC'   : auprc,
            'TPR_YJ'  : tm_yj['TPR'],
            'FPR_YJ'  : tm_yj['FPR'],
            'F1_YJ'   : tm_yj['F1'],
            'TPR_gap_YJ' : fair_yj['TPR_Gap'],
            'FPR_gap_YJ' : fair_yj['FPR_Gap'],
            'PPV_gap_YJ' : fair_yj['PPV_Gap'],
            'DPD_YJ'     : fair_yj['DPD'],
            'AUROC_gap_YJ': fair_yj['AUROC_Gap'],
            'TPR_gap_FX' : fair_fx['TPR_Gap'],
            'FPR_gap_FX' : fair_fx['FPR_Gap'],
            'PPV_gap_FX' : fair_fx['PPV_Gap'],
            'DPD_FX'     : fair_fx['DPD'],
        }
    return metrics

def _threshold_metrics_fast(y_true, y_pred, weights=None):
    """Fast threshold metrics without confusion_matrix."""
    tp = ((y_true==1) & (y_pred==1))
    fn = ((y_true==1) & (y_pred==0))
    fp = ((y_true==0) & (y_pred==1))
    tn = ((y_true==0) & (y_pred==0))

    if weights is not None:
        w = weights
        tp_w = w[tp].sum(); fn_w = w[fn].sum()
        fp_w = w[fp].sum(); tn_w = w[tn].sum()
        tpr = tp_w/(tp_w+fn_w) if (tp_w+fn_w)>0 else 0
        fpr = fp_w/(fp_w+tn_w) if (fp_w+tn_w)>0 else 0
        ppv = tp_w/(tp_w+fp_w) if (tp_w+fp_w)>0 else 0
    else:
        tpr = tp.sum()/(tp.sum()+fn.sum()) \
              if (tp.sum()+fn.sum())>0 else 0
        fpr = fp.sum()/(fp.sum()+tn.sum()) \
              if (fp.sum()+tn.sum())>0 else 0
        ppv = tp.sum()/(tp.sum()+fp.sum()) \
              if (tp.sum()+fp.sum())>0 else 0

    f1 = 2*tpr*ppv/(tpr+ppv) if (tpr+ppv)>0 else 0
    return {'TPR': tpr, 'FPR': fpr, 'PPV': ppv, 'F1': f1}

def _fairness_gaps_fast(y_true, y_prob, threshold,
                        income_groups, weights=None):
    """Fast fairness gap computation."""
    gaps = {'TPR_Gap':0,'FPR_Gap':0,
            'PPV_Gap':0,'DPD':0,'AUROC_Gap':0}

    grp_metrics = {}
    for grp in ['Low', 'Higher']:
        mask = income_groups == grp
        if mask.sum() < 10:
            return gaps
        y_g   = y_true[mask]
        y_p_g = y_prob[mask]
        w_g   = weights[mask] if weights is not None \
                else None
        y_pred_g = (y_p_g >= threshold).astype(int)
        tm = _threshold_metrics_fast(y_g, y_pred_g, w_g)
        try:
            auc_g = roc_auc_score(y_g, y_p_g,
                                  sample_weight=w_g)
        except Exception:
            auc_g = 0.5
        grp_metrics[grp] = {**tm, 'AUROC': auc_g}

        # Prediction rate for DPD
        if weights is not None:
            pred_rate = np.average(
                (y_p_g >= threshold).astype(float),
                weights=w_g)
        else:
            pred_rate = (y_p_g >= threshold).mean()
        grp_metrics[grp]['pred_rate'] = pred_rate

    if 'Low' in grp_metrics and 'Higher' in grp_metrics:
        for m in ['TPR','FPR','PPV','AUROC']:
            gaps[f'{m}_Gap'] = (
                grp_metrics['Low'][m] -
                grp_metrics['Higher'][m])
        gaps['DPD'] = (
            grp_metrics['Low']['pred_rate'] -
            grp_metrics['Higher']['pred_rate'])

    return gaps

