def bootstrap_pvalue(obs_diff, boot_diffs):
    """
    Two-sided p-value from bootstrap distribution.
    p = 2 * min(P(diff >= 0), P(diff <= 0))
    under H0: diff = 0 (shift to center at 0)
    """
    boot_arr   = np.array(boot_diffs)
    # Center bootstrap distribution at 0
    boot_centered = boot_arr - np.mean(boot_arr)
    if obs_diff >= 0:
        p = np.mean(boot_centered >= abs(obs_diff))
    else:
        p = np.mean(boot_centered <= -abs(obs_diff))
    # Two-sided
    p = 2 * p
    return max(p, 1/len(boot_arr))  # floor at 1/B

