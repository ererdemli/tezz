# --- 11.3 Weighted Brier score helper ---
def brier_score(y_true, y_prob, weights=None):
    """Lower is better. Weighted if weights provided."""
    if weights is None:
        return brier_score_loss(y_true, y_prob)
    else:
        w = weights / weights.sum()
        return float(np.sum(w * (y_true - y_prob) ** 2))

# --- 11.4 Val arrays ---
